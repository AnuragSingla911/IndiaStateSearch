public class BaseModal {

    private String mCountry ;

    private String mStateName;

    private String mAbbr;

    private String mArea;

    private String mLargestCity;

    private String mCapital;

    public String getmCountry() {
        return mCountry;
    }

    public void setmCountry(String mCountry) {
        this.mCountry = mCountry;
    }

    public String getmStateName() {
        return mStateName;
    }

    public void setmStateName(String mStateName) {
        this.mStateName = mStateName;
    }

    public String getmAbbr() {
        return mAbbr;
    }

    public void setmAbbr(String mAbbr) {
        this.mAbbr = mAbbr;
    }

    public String getmArea() {
        return mArea;
    }

    public void setmArea(String mArea) {
        this.mArea = mArea;
    }

    public String getmLargestCity() {
        return mLargestCity;
    }

    public void setmLargestCity(String mLargestCity) {
        this.mLargestCity = mLargestCity;
    }

    public String getmCapital() {
        return mCapital;
    }

    public void setmCapital(String mCapital) {
        this.mCapital = mCapital;
    }
}
